"""
Beer Production System – SimPy Simulation
==========================================

COMBINATION ALGORITHM
─────────────────────
A "combination" is a fully pre-enumerated route from Silo to Fermentation Tank:

    Silo → MaltLine → Mill → Mashing[i]
         → Lautering[i] → Prewort[i] → Boiling[i]   (lane i = mashing index)
         → Cooler[j] → FermLine[k] → FermTank (Small | Large)

All 1-to-1 lanes (Mashing → Lautering → Prewort → Boiling) are implicit in
the mashing index, so no separate choice is needed for those stages.

FLAG ALGORITHM  (identical rule applied to the FULL path)
──────────────
    combo.flag = 1  if  ANY equipment in the combo is currently busy.
    combo.flag = 0  only when ALL equipment in the combo is free.

Equipment is freed in stages as each step completes:
    ① MaltLine done   → free Silo, MaltLine
    ② Mill done       → free Mill
    ③ Mashing done    → free Mashing, Lautering, Prewort, Boiling (entire lane)
    ④ Cooling done    → free Cooler
    ⑤ FermLine done   → free FermLine  → combo.flag becomes 0

BATCH CREATION RULE
───────────────────
    1. Find the first available fermentation tank (Small first, then Large).
    2. Collect all combinations whose flag==0 matching that tank_type.
    3. Pick the combination with MINIMUM total processing time.
    4. Lock all equipment in the combination → combo.flag = 1.
    5. Pre-allocate one slot in the target tank.
    6. Spawn the batch pipeline process.
    7. Retry immediately (timeout=0) to fill remaining free slots.

TOTAL COMBINATIONS
──────────────────
    upstream (Silo x MaltLine x Mill x Mashing) : 85
    x Coolers (4) x FermLines (2) x TankTypes (2) = 1 360 combinations
"""

